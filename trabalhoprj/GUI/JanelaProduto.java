    public JanelaProduto(java.awt.Frame parent, OperacaoCadastro operacaoCadastro, Produto produto) {
        super(parent, true);
        confirmado = false;
        this.operacaoCadastro = operacaoCadastro;
        this.produtos = produto;
        initComponents();
        if(operacaoCadastro == operacaoCadastro.ocAlterar || operacaoCadastro == OperacaoCadastro.ocConsultar){
            edNome.setText(produto.obterNome());
            edUnidade.setText(String.valueOf(produto.obterUnidade()));
            edCodigo.setText(String.valueOf(produto.obterCodigo()));
            edPreco.setText(String.valueOf(produto.obterPreco()));
            edUltimaVenda.setText(produto.obterUltimaVenda());
            edQuantidadeEstoque.setText(String.valueOf(produto.obterQuantidadeEstoque()));
        }
        buCancelar.setEnabled(operacaoCadastro != OperacaoCadastro.ocConsultar);
        edNome.setEnabled(operacaoCadastro != OperacaoCadastro.ocConsultar);
        edCodigo.setEnabled(operacaoCadastro != OperacaoCadastro.ocConsultar && operacaoCadastro != OperacaoCadastro.ocAlterar);
        edUnidade.setEnabled(operacaoCadastro != OperacaoCadastro.ocConsultar);
        edPreco.setEnabled(operacaoCadastro != OperacaoCadastro.ocConsultar);
        edUltimaVenda.setEnabled(operacaoCadastro != OperacaoCadastro.ocConsultar);
        edQuantidadeEstoque.setEnabled(operacaoCadastro != OperacaoCadastro.ocConsultar);
    }
    
    public boolean operacaoConfirmada(){
        return confirmado;
    }
    
    public static boolean executar (java.awt.Frame parent,OperacaoCadastro operacaoCadastro, Produto fornecedor){
        JanelaProduto janelaFornecedor = new JanelaProduto(parent, operacaoCadastro, fornecedor);
        janelaFornecedor.setLocationRelativeTo(null);
        janelaFornecedor.setVisible(true);
        return janelaFornecedor.operacaoConfirmada();
    }
