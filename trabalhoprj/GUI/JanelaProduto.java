    private void buOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buOKActionPerformed
        if(edNome.getText().trim().equals("") || edUnidade.getText().trim().equals("") 
        || edPreco.getText().trim().equals("") || edQuantidadeEstoque.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Um dos campos exigidos não está preenchido.","",2);
        } else {
            if(operacaoCadastro != OperacaoCadastro.ocConsultar){
                produtos.atualizarCodigo(Integer.valueOf(edCodigo.getText()));
                produtos.atualizarNome(edNome.getText());
                produtos.atualizarUnidade(Integer.valueOf(edUnidade.getText()));
                produtos.atualizarPreco(Float.valueOf(edPreco.getText()));
                produtos.atualizarUltimaVenda(edUltimaVenda.getText());
                produtos.atualizarQuantidadeEstoque(Integer.valueOf(edQuantidadeEstoque.getText()));

                confirmado = true;
            }
        dispose();
        }
    }//GEN-LAST:event_buOKActionPerformed

